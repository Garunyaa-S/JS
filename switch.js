let vehicle =  "bike";

switch(vehicle) {
    case "car":
        console.log("Car is a Four-wheeled vehicle.");
        break;
    case "bike":
        console.log("Bike is a Two-wheeled vehicle.");
        break;
    case "auto rickshaw":
        console.log("Auto rickshaw is a Three-wheeled vehicle.");
        break;
    case "truck":
        console.log("Truck is a large vehicle for transporting goods.");
        break;
    default:
        console.log("Unknown vehcile type");
}